public class Loop {
    public static void main(String[] args) {
        // loop usando While
        int i = 0;
        while (i < 5) {
            System.out.println(i);
           i++;
        }
        System.out.println("========================");
        // loop usando do while
        int l = 0;
        do {
            System.out.println(l);
            l++;
        }
        while(l < 5);
       
    }
}
