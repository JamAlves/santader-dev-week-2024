

public class NumeroStrings {
    public static void main(String[] args) {
        int x = 10;
        int y = 20;
        int z = x + y;
        System.out.println(z);
        // Adicionando Numeros e Strings
        String x1 = "10";
        String y1 = "20";
        String z1 = x1 + y1;
        System.out.println(z1);

        String x2 = "10";
        int y2 = 20;
        String z2 = x2 + y2;
        System.out.println(z2);
    }
}
